    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().trim().isEmpty() || txtPartido.getText().trim().isEmpty() || 
                txtNumero.getText().trim().isEmpty() || txtCargo.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        } else {
            Candidato candidato = new Candidato();
            if(!txtID.getText().isEmpty()){
                candidato.setIdcandidato(Integer.parseInt(txtID.getText()));
            }
            candidato.setNome(txtNome.getText().trim());
            candidato.setPartido(txtPartido.getText().trim());
            candidato.setNumero(Integer.parseInt(txtNumero.getText().trim()));
            candidato.setCargo(txtCargo.getText().trim());
            String msg;
            if (candidato.getIdcandidato() == 0) {
                msg = candidatoDAO.cadastrarCandidato(candidato);
                if(msg.equals("1")){
                    msg = "CADASTRO REALIZADO COM SUCESSO!";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                msg = candidatoDAO.atualizarCandidato(candidato);
                if(msg.equals("1")){
                msg = "ALTERAÇÃO EFETUADA COM SUCESSO!";
            }
                JOptionPane.showMessageDialog(rootPane, msg);
            }
            this.LimparCampos();
            this.preencherTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

