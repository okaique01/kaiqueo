    public void preencherTabela(){
        ArrayList candidatos;
        DefaultTableModel tabela = (DefaultTableModel)tabelaCandidatos.getModel();
        tabela.setRowCount(0);
        candidatos = candidatoDAO.listarCandidatos();
        Iterator it = candidatos.iterator();
        while(it.hasNext()){
            Candidato candidato = (Candidato)it.next();
            tabela.addRow(new Object[]{candidato.getIdcandidato(), candidato.getNome(), candidato.getPartido(), candidato.getNumero(), candidato.getCargo()});
        }
       
               
    }

